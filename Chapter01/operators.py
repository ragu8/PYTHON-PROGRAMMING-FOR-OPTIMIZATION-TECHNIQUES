# Addition (+)
result_addition = 5 + 3
print(result_addition)  # Output: 8

# Subtraction (-)
result_subtraction = 10 - 4
print(result_subtraction)  # Output: 6

# Multiplication (*)
result_multiplication = 6 * 2
print(result_multiplication)  # Output: 12

# Exponentiation (**)
result_exponentiation = 2 ** 3
print(result_exponentiation)  # Output: 8

# Division (/)
result_division = 15 / 3
print(result_division)  # Output: 5.0

# Modulus (%)
result_modulus = 17 % 4
print(result_modulus)  # Output: 1

# Left Shift (<<) 
result_left_shift = 8 << 1   # Left shift by 1 position (equivalent to multiplying by  2)
print(result_left_shift)  # Output: 16

# Right Shift (>>)
result_right_shift = 32 >> 1  # Right shift by 2 positions (equivalent to dividing by 2)
print(result_right_shift)  # Output: 16

# Left Shift (<<) 
result_left_shift = 8 << 2   # Left shift by 1 position (equivalent to multiplying by  2^2)
print(result_left_shift)  # Output: 32

# Right Shift (>>)
result_right_shift = 32 >> 2  # Right shift by 2 positions (equivalent to dividing by 2^2)
print(result_right_shift)  # Output: 8

# Bitwise AND (&)
'''
0 AND 0 = 0
0 AND 1 = 0
1 AND 0 = 0
1 AND 1 = 1
'''
result_bitwise_and = 5 & 3
print(result_bitwise_and)  # Output: 1

# Bitwise OR (|)
'''
0 OR 0 = 0
0 OR 1 = 1
1 OR 0 = 1
1 OR 1 = 1
'''
result_bitwise_or = 5 | 3
print(result_bitwise_or)  # Output: 7

# Bitwise XOR (^)
'''
0 XOR 0 = 0
0 XOR 1 = 1
1 XOR 0 = 1
1 XOR 1 = 0
'''
result_bitwise_xor = 5 ^ 3
print(result_bitwise_xor)  # Output: 6

# Less Than (<)
result_less_than = 7 < 10
print(result_less_than)  # Output: True

# Greater Than (>)
result_greater_than = 15 > 12
print(result_greater_than)  # Output: True

# Less Than or Equal To (<=)
result_less_than_or_equal_to = 5 <= 5
print(result_less_than_or_equal_to)  # Output: True

# Greater Than or Equal To (>=)
result_greater_than_or_equal_to = 20 >= 18
print(result_greater_than_or_equal_to)  # Output: True

# Equal To (==)
result_equal_to = 3 == 3
print(result_equal_to)  # Output: True

# Not Equal To (!=)
result_not_equal_to = 5 != 7
print(result_not_equal_to)  # Output: True


